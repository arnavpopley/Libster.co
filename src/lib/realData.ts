export const TERMS = [
  { name: "Spring Term 2025", start: "2025-01-04", end: "2025-03-21" },
  { name: "Summer Term 2025", start: "2025-04-26", end: "2025-06-27" },
  { name: "Autumn Term 2025", start: "2025-09-27", end: "2025-12-12" },
] as const;

// Helper to get term for a date
export function getTermForDate(date: Date): string {
  const dateKey = `${date.getFullYear()}-${String(date.getMonth() + 1).padStart(2, '0')}-${String(date.getDate()).padStart(2, '0')}`;
  for (const term of TERMS) {
    if (dateKey >= term.start && dateKey <= term.end) {
      return term.name;
    }
  }
  return "Outside term time";
}